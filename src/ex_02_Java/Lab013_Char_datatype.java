package ex_02_Java;

public class Lab013_Char_datatype {

    public static void main(String[] args) {
        char c = 10; //this is possible because it is integral data type --> ASCII

        System.out.println(c); // this will not print anything because no char is there for 10 ascii value

        char a = 97; // this is ASCII value of a

        System.out.println(a); //this will print a
    }
}
